# List არის mutable (შეცვლადი), რაც ნიშნავს, რომ ელემენტების შეცვლა, წაშლა და დამატება შესაძლებელია.
# Tuple უკეთესია იმ მონაცემებისთვის, რომლებიც ფიქსირებული და არ ცვალებადი უნდა იყოს.
# List უფრო მოქნილია, როდესაც მონაცემების ცვლილებები საჭიროა.
