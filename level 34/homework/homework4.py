# Mutable (შეცვლადი) ნიშნავს, რომ მონაცემთა სტრუქტურაში (მაგ., list, dict) შესაძლებელია ელემენტების დამატება, შეცვლა ან წაშლა.
# Immutable (შეუცვლელი) ნიშნავს, რომ მონაცემთა სტრუქტურა (მაგ., tuple, string) ერთხელ შექმნის შემდეგ ვეღარ შეიცვლება.
# მაგალითი:
# Mutable: list -> my_list = [1, 2, 3]; შეგვიძლია დავამატოთ ან წავშალოთ ელემენტები.
# Immutable: tuple -> my_tuple = (1, 2, 3); ვერ შევცვლით ელემენტებს.
